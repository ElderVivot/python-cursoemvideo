n1 = float(4.65)
n2 = int(2)
n3 = int(3)
exponenciacao = n1 ** n3 # exponenciacao
divisao_int = n1 // n3 # divisao inteira
resto_div = n1 % n2 # resto divisao
print('A exponenciação é {:.2f}, a divisão inteira é {:.1f} e o resto da divisão é {:.4f} '
      ''.format(exponenciacao, divisao_int, resto_div))